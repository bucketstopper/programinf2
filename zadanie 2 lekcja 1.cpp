#include <iostream>
using namespace std;
 
int main() {
    
    int a, b;
 
  
    cout << "Liczba a: ";
    cin >> a;
 
    cout << "Liczba b: ";
    cin >> b;
 
    
    int suma = a + b;
    int roznica = a - b;
    int iloczyn = a * b;
 
   
    cout << "Suma: " << suma << endl;
    cout << "Roznica: " << roznica << endl;
    cout << "Iloczyn: " << iloczyn << endl;
 
    return 0;
}

